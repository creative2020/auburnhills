                    <div align="center"><strong><h1>Auburn Hills Swimming Facility Master Association (AHSFMA)</br>
                    Clubhouse Rules and Regulations</strong></h1></div>
                  

<p>1.	The Legends and Meadows Clubhouses are available to all AHSFMA (Auburn Hills Swimming Facility Master Association) members for private parties on any day or evening (except those holidays designated below), provided that it does not interfere with the normal operations of the Clubhouse. The member must be in good standing (i.e. all dues and assessments are current) at the time in which the reservation is made and at the time of the event for which the clubhouse is reserved.</p> 

<p>2.	Reservation forms Clubhouse and pool availability are accessible on the AHSFMA's web site: <strong>www.auburnhillspools.com.</strong> To reserve the Clubhouse or Pool, complete the required information on the form and click "submit" when finished. Reservations are approved on a first-come, first-served basis and notification of approval will be via e-mail.</p>

<p>3.	The Clubhouse and Pool Reservation Calendar will be maintained by the Clubhouse Manager and updated on a regular basis as a reservation is received and confirmed. Reservations will not be confirmed until a $100 deposit and any required multi-use fee is received.  The deposit and all other fees must be received within 7 days of reservation request or the request will be cancelled without further notice.</p>

<p>4.	All cancellations require a two-week notification or a $25.00 fee will be assessed. A six-week cancellation notice is required for reservation dates between Thanksgiving and New Year's Day.  Reservations cannot be made until dues invoices are mailed and payment is made in full for the current year.  Reservations cannot be made more than 90 days in advance of the event date.</p>

                    <p>5.	The Auburn Hills Swimming Facility Master Association consists of 6 units: (1) Enclave adult/kid pool, (2) Fairways pool, (3) Legends pool, (4) Meadows adult/kid pool, (5) Legends Clubhouse and (6) Meadows Clubhouse.  <span style="text-decoration:underline;"><strong>Each AHSFMA member may reserve one unit, one time annually free of charge.</strong></span> All additional reservations of <strong>each</strong> unit are $75.00, which must be received within 7 business days of reservation request.   Approval for these meetings or functions must be made through the Clubhouse Manager.  (Revised 1/01/2015)</p>

<p>6.	The $100.00 deposit fee will be refunded after the Clubhouse has been inspected and it has been determined that proper cleanup has occurred, no damage was done, and the key is returned.  Failure to properly clean the Clubhouse facilities as specified after a function will result in a fee.  Refer to the Clubhouse Cleanup as specified after a function will result in a fee of $45 to be withheld from the deposit.  Refer to rule No. 19 for cleanup requirements.</p>  

<p>7.	The Clubhouse cannot be reserved for private functions on the following holidays: Memorial Day, July 4, or Labor Day.</p>

                    <p>8.	Reservations for the Clubhouse <strong>do not</strong> include the pool or pool area, unless reserved on a separate pool reservation form. Legend's and Meadow's pool may be reserved with the Clubhouse only on Saturday evening between 5:00pm & 10:00pm, with an additional charge.  See section 5 for detail of charges. (If pool is not reserved; members may only bring up to (5) guests to the pool)</p>

<p>9.	The member who reserves the Clubhouse assumes full responsibility for the conduct of their guests and must be in attendance at all times during the event for which the Clubhouse was reserved. The member reserving the Clubhouse is responsible and liable for any damage done to the Clubhouse including any damage caused by party decor.</p>  

<p>10.	Noise must be kept within the confines of the facility. No speakers or other electronic sound equipment may be placed outside the facility. The volume of all music must be kept to a minimum. Fireworks are prohibited. Complaints by the residence within the vicinity of the Clubhouse may result in termination of the event. Any occurrence of activities, which result in such termination, shall be deemed to be a breach of the rules by the member and a $100.00 fee will be assessed.</p>
 
<p>11.	NO TAPE MAY BE PLACED ON THE CEILING OR WALLS OF THE CLUBHOUSE AND NO PICTURES OR DECORATIONS MAY BE HUNG WITHIN THE FACILITIES, UNLESS MEMBER RECEIVED ADVANCE AUTHORIZATION FROM THE ASSOCIATION'S MANAGER. NOTHING IS TO BE ATTACHED TO THE FANS (NO BALLOONS).  A BREACH OF THESE CONDITIONS BY THE RESIDENT WILL RESULT IN THE FORFEIT OF THE DEPOSIT.</p>

<p>12.	Appropriate attire is required and wet bathing suits are prohibited inside the Clubhouse.</p>

<p>13.	All events may start at 10:00 A.M. and end by 12:00 (midnight). The Clubhouse will close promptly at 12:00 (midnight).</p>

<p>14.	No activity or event for which the Clubhouse is used may be advertised or publicized in any manner to solicit the attendance of the general public. No admission may be charged at the door.</p>

<p>15.	The Clubhouse cannot be used to conduct any business unless deemed permissible by the Clubhouse Manager in advance.</p>

<p>16.	No birdseed, rice, or glitter of any type or confetti may be thrown inside the facility or the pool area. Confetti of any kind is strictly prohibited inside or outside the facility. Use of fore-mentioned material will result in the forfeit of the deposit to cover the additional cost of the cleanup.</p>

<p>17.	No pets are allowed inside the Clubhouse or Pool.</p>

<p>18.	No Smoking inside or in front of the Clubhouse. </p> 

<p>19.	Clubhouse Cleanup Guidelines are located inside each Clubhouse.  Example listed below:</p>
<ul class="abc">
    <li>A. All floors must be mopped or cleaned where any spills occurred.</li>
    <li>B. All floors and rugs are to be swept or vacuumed.</li> 
    <li>C. All countertops, cabinets, tables and chairs must be wiped clean.</li> 
    <li>D. The Kitchen must be properly cleaned, including emptying the trash, cleaning sinks, and cleaning the interior of the refrigerator, stove and microwave. (if used)</li>
    <li>E. The Restrooms must be properly cleaned, including emptying the trash, cleaning sinks, vanities, mirrors and toilets.</li>
    <li>F. Any marks or food spills made on the interior walls and doors must be wiped clean. </li>
    <li>G. All food must be removed.</li>
    <li><span style="text-decoration:underline;"><strong>H. You must take all of your trash with you and replace the trash bags.</strong></span></li>
    <li>I. All porch/patios must be swept and trash removed.</li>
    <li>J. If pool was reserved than the pool area must be left clean and all trash removed.</li>
    <li>K. All lights must be turned off except for designated lights on the outside of the building.</li>
    <li>L. The heating/air-conditioning thermostat must be returned to the settings indicated on the lockbox and the lockbox secured.</li>
    <li>M. All doors must be closed and locked when leaving. </li>
    <li>N. All party signs placed outside of clubhouse and around neighborhood must be removed promptly after the event.</li>
    <li>O. Cleanup must be completed immediately after the event unless pre-arranged with the Clubhouse Manager. If next-day cleanup is approved, it must be completed by 9:30 a.m. </li>
</ul>
<p>20.	HOA Management Service and the President of the Association will retain the clubhouse keys.  There is a lock box attached to the clubhouse on the right side of the front door with a key inside of it.  When a member reserves a clubhouse, the member will be emailed a four (4) digit code that will open the lockbox.  Once your event is over, lock all doors and return the key to the lockbox.  If the key is lost, the member is responsible and liable for all cost and expenses for changing the affective locks.  DO NOT DUPLICATE THE KEY. </p>  

<p>21.	The use of alcohol will be at the discretion of the member reserving the Clubhouse and must be controlled by that member. <strong>No underage drinking will be allowed.</strong> The Association will not be liable for any accident or incident related to the use of alcohol on the premises.</p>

                    <p>22.	Members understand and agree to abide by all local, state and federal laws and ordinances, which pertain to residents and guests while using the Clubhouse. <strong>The Association will not be liable for any incidents related to the abuse of such laws and ordinances.</strong></p>

                    <p>23.	<strong>The Association reserves the right to refuse the use of the Clubhouse for any purpose it deems to be detrimental to or not in the best interest of its members. It further reserves the right to refuse the use of the Clubhouse on any day and at any time it deems inappropriate or inconvenient.</strong></p>
