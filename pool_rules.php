<p>All members of the AHSFMA should be able to use the pool and clubhouse facilities in a safe and enjoyable manner. To clarify the existing pool rules and regulations, the following actions were adopted and approved by the AHSFMA Board of Directors.</p>

<p>1. There will be no lifeguard on duty. Therefore, persons using the pool and pool area do so at their own risk. <strong>Wichita City Codes requires that "children under the age of 16 must be accompanied by an adult (18 years of age, parent or guardian)".</strong> Failure to comply with this rule will result in immediate suspension of pool access for 15 days.</p>

<p>2. The Auburn Hills Swimming Facilities Master Association pools are PRIVATE and reserved for the use of members and their guests only. </p>

<p>3. A member must accompany all guests. The maximum number of guest per household is five (5) except when the pool is reserved for a private party.</p>

<p>4. All pools will open 10:00 a.m. and close at 10:00 p.m.  Access cards will not open gates before 10:00 a.m. or after 10:00 p.m.  Security System will be activated.  Do Not prop open the gates.</p>

<p>5. Suntan oil is prohibited in all pools.</p> 

<p>6. Per state law, persons with infections, open sores or communicable diseases will not be allowed in the pools.</p>

<p>7. Proper swimming attire is required (no cut-off jeans, etc.)</p>

<p>8. Loud music, running, rough play, splashing outside pool, cannonball jumping, standing on or jumping from shoulders, towel flipping, dunking, foul language, or "crying wolf" will not be tolerated.</p>

<p>9. Eating, drinking, or gum chewing is not permitted while in the water.  No towels allowed in the water.</p>

<p>10. NO SMOKING in the pool area.</p>

<p>11. The use of pools toys (air mattresses and tubes) will be supervised with discretion.  Do not throw hard or sharp objects that could damage pool liner. (Example: toy torpedoes and toy rockets)</p>

<p>12. No pets, bikes or skateboards are allowed inside the pool fence. Bikes should be parked outside the pool area and off the sidewalks.</p>

<p>13. NO GLASS or ALCOHOLIC beverages are allowed in the pool area. All beverages, lotions, etc. must be in unbreakable containers. </p> 

<p>14. No one should be in the pool(s) during a thunderstorm or while it is lightning.</p>

<p>15. The Master Pool Association is not responsible for personal property.</p>

<p>16. Reservations for the Pool <strong>do not</strong> include the clubhouse, unless reserved on a separate clubhouse reservation form.  A Pool Reservation Form must be completed and submitted before a pool may be reserved.  The Enclave pool may be reserved for private parties on Sunday evening.  The Fairways pool may be reserved for private parties on Friday evening. The Legends and Meadows pool may be reserved for private parties on Saturday evenings. All pool reservations are between 5:00p.m. until 10:00p.m.  The pool must be reserved in advance and the member hosting the party is responsible for cleaning pool area after the party.  Each AHSFMA member may reserve one unit, one time annually free of charge.  All additional reservations of <span style="text-decoration:underline;"><strong>each</strong></span> unit are $75.00.  Please reference Clubhouse Rules and Regulations for details on reserving the Clubhouses. </p> 

<p>17. Damage to the pool, liner or furniture will be the responsibility of the member hosting the party.</p>

<p>18. Anyone who violates the rules will be subject to loss of pool privileges.</p>

<p>19. The last person to leave the pool area should ensure the restroom lights are off, restroom door and pool gates are locked before leaving. All trash in the pool area should be picked up.</p>

<p>20. The pool member and/or guests shall provide their name and address upon request.</p>

<p>21. There will be a $25 charge for replacing a lost pool card.</p>

<p>22. Any person caught vandalizing the property, equipment or vending machines will be prosecuted.</p>

<p>23. The pool privileges of any member whose pool dues or other assessments are delinquent may have their pool privileges suspended.</p>

<p>24. Pool Key activation will occur once a week during pool season on Friday morning prior to 9:00am.  To allow entry on Friday and afterward, any member's account must be paid in full by 3:00p.m. on Thursday.  There are no exceptions to this policy and no temporary badges will be issued.</p>

<h2>POOL RULES ENFORCEMENT</h2>

<p>Each AHSFMA member is expected to ensure that the members of their family and guests are following the pool rules. The elected board members and committee members of the Auburn Hills Master Swimming Facility Association are responsible for the enforcement of the rules.</p>

<h3>To report a pool rule violation, the following steps should be taken:</h3>
<p>1. Document the violation(s) including the name(s) of the person(s) committing the violation, and those who witnessed the violation(s). Include the date and the time of the occurrence and briefly describe the rule(s) violation, print and sign your name.</p>

<p>2. Email to Rodney Wright at <a href="mailto:rzwright@homeownermgmt.com">rzwright@homeownermgmt.com</a> or Mail to Auburn Hills Swimming Facilities Master Association, C/O HOA Management Services, 900 N. Tyler, Ste. 7, Wichita, KS 67212. </p>

<p>The Board of Directors will review the documented violations. Homeowners that have been cited for a pool or clubhouse rule(s) violation will be invited to attend the meeting when the Board reviews the violation(s).</p>

<p>Violations that are upheld by the Board of Directors will be confirmed in writing to the homeowner involved, as well as the homeowner reporting the violation.</p>

<p>If the homeowner or their guests are determined to be in violation of the pool rules a total of three times, their pool privileges will be suspended for a period of one year from the date of the last occurrence.</p>

                    <p>Any homeowner who continues to use the pool or clubhouse facilities after their pool or clubhouse privileges have been suspended by the Board of Directors will be subject to a $500 fine assessed to the homeowner by the Auburn Hills Swimming Facilities Master Association, as well as any and all necessary legal cost to enforce the suspension and fine.</p>

                    <h3>If you are threatened, feel in danger or witness vandalism - CALL 911. Record the person's description, vehicle type & tag number. </h3>

